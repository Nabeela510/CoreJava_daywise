package com.destination.september_batch_programming.sep20;

public class LoopsExample {
	public static void main(String[] args) {
		System.out.println("for loop example");
		for(int i=1;i<=10;i++)
		{
			System.out.println("Nabeela");
		}
		System.out.println("----------------------------------");
		System.out.println("while loop wxample");
		int i=1;
		while(i<=10)
		{
			System.out.println("nabeela");
			i++;
		}
		System.out.println("----------------------------------");
		System.out.println("do...while loop example");
		int j=1;
		do {
			System.out.println("Nabeela");
			j++;
		}while(j<=10);
		System.out.println("----------------------------------");
		System.out.println("do...while loop example 2 ");
		j=1;
		do {
			System.out.println("Nabeela");
			j++;
		}while(j<=-1);
	}

}
